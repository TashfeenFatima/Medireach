        <h3><em>Here’s what to do next:</em></h3>

        1. Get the stinger out immediately. This will prevent additional venom from getting into the person. 
        To remove a stinger, it is best to use a straight-edged object 
        such as a credit card to scrape the stinger out of the skin. 
        Avoid squeezing the venom sac with tweezers or your fingers, as this can inject venom into the skin. <br><br>
        2. Wash the area with soap and water.<br><br>
        3. Use a cold pack to help with the swelling at the site; however, do not apply ice directly to the skin.<br><br> 
        4. Use an allergy medication or antihistamine (like Benadryl) to reduce swelling and itching. <br><br>
        5. Use Tylenol (acetaminophen) or Advil (ibuprofen) for pain.<br>
