        <div style="font-size: larger;">
            <p>Cardiopulmonary resuscitation (CPR) is a lifesaving technique that's useful in many 
                emergencies in which someone's breathing or heartbeat has stopped.<br>For example, 
                when someone has a heart attack or nearly drowns.<br>
                When a person initiates CPR immediately after someone’s heart stops beating, 
                CPR can double or even tripleTrusted Source the chances of them surviving.</p>
    
        <p><B>A person of any age needs CPR if they:</B></p>
        <img src="image/cpr3.png" style="float:right" alt="cpr1" height="400" width="400">
            <ul>
                <li>Collapse.</li>
                <li>Don’t respond.</li>
                <li>Aren’t breathing.</li>
                <li>Don’t have a pulse.</li>
            </ul>
        <h2><em>How should I prepare for CPR?</em></h2>
        Before starting CPR, follow these steps:<br>
        <ol><li>If you see someone collapse, loudly ask the person if they’re OK.</li>
        <li>If they don’t respond, call 911 and ensure a safe scene. The 911 dispatcher can guide you through the steps to take until paramedics arrive.</li>
        <li>Ask someone nearby to get an automated external defibrillator (AED).</li>
        <li>Tilt the person’s head back while they’re lying on their back.</li>
        <li>Lean in close to the person’s face and listen for 10 seconds to see if you hear them breathing.</li>
        <li>Check to see if the person is breathing or to see if their chest is going up and down.</li>
        <li>Check for a pulse by feeling the side of their neck.</li>
        <li>Perform CPR if you don’t feel a pulse.</li>
        </ol>
        <h2><em>How to do CPR?</em></h2>
        <B>Follow these CPR instructions to help someone older than an infant:</B><br>
        <ol>
        <li>If the person isn’t breathing, put one of your hands over the other and place them in the middle of the person’s chest (right under their nipples). If you’re helping a child up to age 8, use one hand and place it right above the bottom of their breastbone.</li>
        <img src="image/cpr2.jpg" alt="cpr1" style="padding-left:250px" height="300" width="300"><br>
        <li>Putting the force of your body weight behind it, push your hands down hard in the middle of the person’s chest. Use the heel of your hand, or the part just before your wrist. Keep your arms straight.</li>
        <li>Keep pushing on the person’s chest (chest compressions) 100 to 120 times per minute, pushing down 2 inches (about the height or short side of a credit card) each time. Make sure you allow their chest to come all the way back up between compressions.</li>
        <li>People who have CPR training can pause compressions to give the person two mouth-to-mouth rescue breaths for every 30 compressions (about 20 seconds or so).</li>
        <li>Keep doing chest compressions and giving rescue breaths in a cycle until the person revives or more help arrives.</li>
        </ol><br>
        <h3><em>Perform the rescue breath as follows:</em></h3>
        <ol>
        <li>Pinch the person’s nose closed while tilting their head back a little and their chin up.</li>
        <li>Close your mouth over theirs and blow a normal-sized breath into it so their chest goes up. If the person’s chest doesn’t come up, check to see if there’s something in their mouth.</li>
        <li>Give a total of two breaths and go back to doing compressions.</li>
        <li>While you’re doing CPR, someone should be bringing an AED to use to help with resuscitating the person.</li>
        </ol>
        <img src="image/cpr4.jpg" alt="cpr1" style="padding-left:250px" height="300" width="300"><br>
        <br>Repeat the cycle of 30 chest compressions and two rescue breaths until the person starts breathing or help arrives.
        If an AED arrives, carry on performing CPR until the machine is set up and ready to use.<br><br>
        <B><em>When Should You Not Do CPR?</em></B><br>
        <ul><li>If someone is conscious and breathing, they don’t need CPR.</li><B> There are also some specific situations when you should not try to give CPR.</B>

        <li>The first is when it would put you in danger. You might be thinking more about trying to help someone, but you need to consider your own safety first.

        Do not begin CPR if the scene is unsafe for the person providing CPR or places the rescuer at risk.</li>
        <li>Another situation is when a person would not want to get CPR. For example,
            people with end-stage chronic health conditions may have made a decision with their providers that if they go into cardiac arrest,
            they do not want any life-sustaining efforts made to save them. This is called a do-not-resuscitate (DNR) order.</li>
        <li>Stop after More Than 30 Minutes of CPR.</li>
        <li>If you're afraid to do CPR or unsure how to perform CPR correctly, 
            know that it's always better to try than to do nothing at all.
            The difference between doing something and doing nothing could be someone's life.</li>
